async function ajaxCall(endPoint,methord,data) {
  $.ajax({
      url: endPoint, // Replace with your endpoint
      type: methord,
      data: data,
      success: function (response) {
          console.info('result', response);
          return response;
      },
      error: function (xhr) {
          console.debug('ERROR', xhr);
          return xhr;
          
      },
  });
}

